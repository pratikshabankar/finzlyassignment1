package dowhileloop;

public class Dowhileloop29 {
    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.print(i + " ");
            i++;
        } while (i <= 5);

        i = 4;

        do {
            System.out.print(i + " ");
            i--;
        } while (i >= 1);
    }

}
