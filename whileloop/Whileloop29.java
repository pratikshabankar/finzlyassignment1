package whileloop;

public class Whileloop29 {
    public static void main (String [] args){
        int i = 1;
        while(i<=5){
            System.out.println(i +" ");
            i++;
        }
        int p =4;
        while(p>=1){
            System.out.println(p  +" ");
            p--;


        }

    }
}
